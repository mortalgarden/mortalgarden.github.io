                        const samamaModal = document.getElementById("SamamaKhalid");
function samamaShow() {
  samamaModal.showModal();
}
function samamaClose() {

  samamaModal.classList.add('hide');

  samamaModal.addEventListener('webkitAnimationEnd', function() {
    samamaModal.classList.remove('hide');
    samamaModal.close();
    samamaModal.removeEventListener('webkitAnimationEnd',  arguments.callee, false);
  }, false);
}
