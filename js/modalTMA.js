                              var dialog;
                              const body = document.querySelector("body");
function openCheck(dialog) {
  if (dialog.open) {
    body.classList.add('open-modal');
  } else {
    body.classList.remove('open-modal');
  }
};
// MAGNUS INSTITUTE EMPLOYEES
                      const jonModal = document.getElementById("JonathanSims");
                      
function jonShow() {  var dialog = jonModal;
  jonModal.showModal();
  openCheck(dialog);
}
function jonClose() {  var dialog = jonModal;

  jonModal.classList.add('hide');

  jonModal.addEventListener('webkitAnimationEnd', function() {
    jonModal.classList.remove('hide');
    jonModal.close();
    openCheck(dialog);
    jonModal.removeEventListener('webkitAnimationEnd',  arguments.callee, false);
  }, false);
}
